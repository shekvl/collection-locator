package com.anonymizerweb.anonymizerweb.structures;

public class Interval {
    String text;
    Double value;

    public Interval() {
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public Double getValue() {
        return value;
    }

    public void setValue(Double value) {
        this.value = value;
    }
}
